int		ft_count_sep_words(char const *s, char c)
{
	int		i;
	int		n;

	if (!s)
		return (0);
	i = 0;
	n = 0;
	if (s[0] && s[0] != c)
		n++;
	while (s[i])
	{
		if (s[i] == c && s[i + 1] != c && s[i + 1])
			n += 1 + 0 * i++;
		else
			i++;
	}
	return (n);
}
