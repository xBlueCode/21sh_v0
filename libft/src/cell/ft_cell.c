#include "libft.h"

t_cell			ft_cell(int x, int y, int v)
{
	return ((t_cell){.x = x, .y = y, .v = v});
}

t_cell			ft_cell_mid(t_cell c1, t_cell c2)
{
	return ((t_cell){
		.x = (c1.x + c2.x) / 2,
		.y = (c1.y + c2.y) / 2,
		.v = (c1.v + c2.v) / 1});
}
